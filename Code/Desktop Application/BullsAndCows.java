    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        textField = jTextField1.getText().toString();
        
        if (textField.length() == first) {
            if (textField.equals(string)) {
                bulls = stringBuilder.length();
            } else {
                for (int i = 0; i < textField.length(); i++) {
                    if (string.contains(String.valueOf(textField.charAt(i)))) {
                        cows++;
                    }
                    if (textField.charAt(i) == string.charAt(i)) {
                        bulls++;
                    }
                }
            }
            
            turns++;
            jLabel3.setText("Turn " + turns);
            
            if (bulls >= first) {
                int decision = JOptionPane.showConfirmDialog(rootPane, "Hurry! You Won. Do you want to continue ?", "Congractulations You Won", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
                if (decision == JOptionPane.YES_OPTION) {
                    jTextArea1.setText("");
                    jTextField1.setText("");
                    bulls = 0;
                    cows = 0;
                    turns = 1;
                    list.clear();
                    PlayGame();
                    jLabel3.setText("Turn " + turns);
                } else {
                    GameOptions gameOptions = new GameOptions();
                    this.dispose();
                    gameOptions.setVisible(true);
                }
            } else {
                if (bulls > 1 && cows > 1) {
                    jTextArea1.append(String.format("Grade: %d bulls and %d cows\n", bulls, cows));
                } else if (bulls > 1 && cows <= 1) {
                    jTextArea1.append(String.format("Grade: %d bulls and %d cow\n", bulls, cows));
                } else if (bulls <= 1 && cows > 1) {
                    jTextArea1.append(String.format("Grade: %d bull and %d cows\n", bulls, cows));
                } else {
                    jTextArea1.append(String.format("Grade: %d bull and %d cow\n", bulls, cows));
                }
                bulls = 0;
                cows = 0;
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "The length of the input should be " + first + " not " + textField.length() + ".", "Alert Wrong Input!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

