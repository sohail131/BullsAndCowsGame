    public static void PlayGame() {
        turns = 0;
        bulls = 0;
        cows = 0;
        stars = new String(new char[first]).replace("\0", "*");
        stringBuilder = new StringBuilder();
        string = "";
        
        for (int i = 0; i < second; i++) {
            list.add(String.valueOf(symbols.charAt(i)));
        }
        
        for (int i = 0; i < first; i++) {
            stringBuilder.append(list.get(random.nextInt(second)));
        }
        string = stringBuilder.toString();
        
        if (second < 11) {
            text = String.format("%s (%s-%S)", stars, symbols.charAt(0), symbols.charAt(second));
        } else if (second < 36) {
            text = String.format("%s (%s-%S, %s-%s)", stars, symbols.charAt(0), symbols.charAt(9), symbols.charAt(10), symbols.charAt(second));
        } else {
            text = String.format("%s (%s-%S, %s-%s, %s-%s)", stars, symbols.charAt(0), symbols.charAt(9), symbols.charAt(10), symbols.charAt(35), symbols.charAt(36), symbols.charAt(second));
        }
    }

